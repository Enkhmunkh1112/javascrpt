const FLE = (A, B, C, D, E, F) => {
  if (A > B && A > C && A > D && A > E && A > F ) {
    return (A);
  } else if (B > C && B > D && B > E && B > F &&  B > A) {
    return (B);
  }else if (C > B && C > A && C > D && C > E && C > F) {
    return (C);
  } else if (D > C && D > B && D > E && D > F && D > A) {
    return (D);
  }else if (E > B && E > C && E > D && E > A && E > F) {
    return (E);
  } else if (B > C && B > D && B > E && B > F && B > A) {
    return (F);
  }
};

